import { useContext } from "react";
import Head from "next/head";
import Layout from "~/components/Layout";
import Navbar from "~/components/Navbar/Navbar";
import { RiSearchLine } from "react-icons/ri";
import { useSession } from "next-auth/react";
import { useRouter } from "next/router";
import Xlogo from "~/components/Xlogo";
import { NavbarContext } from "~/context/NavbarContext";

export default function Home() {
  const router = useRouter();
  const { data: session, status } = useSession();
  const { setIsOpen } = useContext(NavbarContext);

  if (status === "loading") {
    return <p>Loading...</p>;
  }

  if (status === "unauthenticated") {
    router.push("/signin?redirect=/");
    return <p>Access Denied</p>;
  }

  if (!session?.user.isRegistered) {
    router.push("/register?redirect=/");
  }

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout>
        <Navbar />
        <div className="bg-white lg:border-x lg:border-gray-400">
          <header>
            <div className="relative flex items-center justify-center p-4">
              <button
                onClick={() => setIsOpen(true)}
                className="absolute left-4"
              >
                <img
                  className="rounded-full"
                  src={session?.user.profilePicture}
                  width={32}
                  height={32}
                />
              </button>
              <Xlogo className="h-7" />
            </div>
            <div className="flex">
              <button>For you</button>
              <button>Following</button>
            </div>
          </header>
          <div className="h-screen">a</div>
          <div className="h-screen">a</div>
          <div className="h-screen">a</div>
        </div>
        <div className=" bg-gray-300">
          <form className="relative">
            <RiSearchLine className="absolute left-0 top-0 ml-1 mt-1" />
            <input
              className="w-full pl-6"
              type="search"
              placeholder="Search..."
            ></input>
          </form>
          <div className="h-screen">a</div>
          <div className="h-screen">a</div>
        </div>
      </Layout>
    </>
  );
}
